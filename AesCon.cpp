#include "stdafx.h"
#include "aes.h"

int _tmain(int argc, _TCHAR* argv[])
{
	std::cout << "Advanced Encryption Standart - AES on C++\n\n";

	unsigned char pKey[]={0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,
						   0x0b,0x0c,0x0d,0x0e,0x0f,0x10,0x11,0x12,0x13,0x14,0x15,
						   0x16,0x17,0x18,0x19,0x1A,0x1B,0x1C,0x1D,0x1E,0x1F};
	
	unsigned char pData[]={0x00, 0x11, 0x22, 0x33, 0x44, 0x55,0x66,0x77,0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff};
	unsigned char pCipher[16];

	CAesCrypt AES;
	AES.EnableDump();
	bool bKey=AES.SetKey(pKey, 256);
	if(bKey)
	{
		AES.Crypt(pData, pCipher);
		AES.Decrypt(pCipher, pData);
	}

	std::cout << AES.GetDump();
	std::cout <<"\nPress ENTER to exit ..."; std::cin.get();
	return 0;
}
